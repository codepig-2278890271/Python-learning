# ============================================================
# 认识 Python —— 你的第一个程序
# ============================================================
print("\n" + "=" * 60)
print("你的第一个 Python 程序")
print("=" * 60)

"""
Python 是什么？
  - 一种编程语言，就像你用来和计算机说话的"外语"
  - 语法简单、接近英语，特别适合新手
  - 能做网站、数据分析、人工智能、自动化脚本……几乎无所不能

print() 是什么？
  - 它是 Python 内置的"打印函数"
  - 括号里写什么，屏幕上就显示什么
  - 文本要用引号括起来（单引号 ' 或双引号 " 都可以，但必须成对出现）
"""

# 你的第一行代码！试着运行它：
print("Hello, Python! 你好，世界！")

# 你可以打印任何你想说的话：
print("今天是我学 Python 的第一天！")
print("加油！💪")

# 数字不需要引号，可以直接打印：
print(2024)
print(3.14)

# print() 也可以什么都不打印，就输出一个空行：
print()

# ============================================================
# 注释 —— 写给人类看的文字
# ============================================================
print("=" * 60)
print("注释")
print("=" * 60)

"""
注释是什么？
  - 注释是写给自己（和其他人）看的说明文字
  - 计算机执行代码时会完全忽略注释
  - 好的代码一定要写注释，不然过几天你自己都看不懂！

Python 中有两种注释：
  1. 单行注释：用 # 开头（你现在看到的大部分灰色文字都是）
  2. 多行注释：用三个引号包裹（就像你现在正在读的这段话）
"""

# 这是一个单行注释——解释下面这行代码的作用
name = "小明"  # 这也是注释——可以写在代码的右边
print(name)     # 打印变量 name 的值

"""
这是多行注释。
可以写很多行。
用三个双引号（或三个单引号）把文字包起来就行。
"""

print("\n" + "=" * 60)
print("✅ 本文件学习完成！接下来请打开：02_变量与数据类型.py")
