print("\n" + "=" * 60)
print("第三章：变量")
print("=" * 60)

"""
变量是什么？
  - 想象你有一个储物柜，每个格子里可以放一样东西
  - 变量就是那个储物柜的"标签"，你通过名字来存取里面的东西
  - 等号 = 叫"赋值"，意思是把右边的值放进左边的变量里
    （注意：= 不是数学里的"等于"！）

变量命名规则：
  ✅ 可以用字母、数字、下划线
  ✅ 区分大小写（name 和 Name 是两个不同的变量）
  ❌ 不能用数字开头
  ❌ 不能用 Python 的关键字（比如 print、if、for）
"""

# 基本用法：把值存到变量里
my_name = "小明"      # 字符串（文本）用引号
my_age = 25            # 整数不用引号
my_height = 175.5      # 小数（Python 里叫浮点数）
is_student = True      # 布尔值：True（真）或 False（假），注意首字母大写

# 使用变量：直接写变量名就行
print("我叫", my_name)
print("我今年", my_age, "岁")
print("我身高", my_height, "厘米")
print("我是学生吗？", is_student)

# 变量的值可以改变（所以叫"变"量）
my_age = 26            # 过了一年，年龄变了
print("过了一年，我", my_age, "岁了")

# 好玩的事情：变量之间可以互相赋值
a = 10
b = a                   # b 也得到了 10
print("a =", a, ", b =", b)

# 一行给多个变量赋值
x, y, z = 1, 2, 3
print("x =", x, ", y =", y, ", z =", z)

# ============================================================
# 第四章：数据类型 —— 计算机认识的各种"东西"
# ============================================================
print("\n" + "=" * 60)
print("第四章：数据类型")
print("=" * 60)

"""
Python 有几种基本的数据类型：

  1. 字符串 str    —— 文本，如 "hello"
  2. 整数 int       —— 整数，如 42, -7, 0
  3. 浮点数 float   —— 小数，如 3.14, -0.5, 2.0
  4. 布尔值 bool    —— True 或 False（只有这两个值）
  5. NoneType       —— None，表示"什么都没有"

用 type() 函数可以查看一个值或变量的类型：
"""

# 来看看每种类型
text = "Hello Python"
integer = 100
decimal = 3.14159
truth = True
nothing = None

print("text 的值是:", text, "→ 类型是:", type(text))
print("integer 的值是:", integer, "→ 类型是:", type(integer))
print("decimal 的值是:", decimal, "→ 类型是:", type(decimal))
print("truth 的值是:", truth, "→ 类型是:", type(truth))
print("nothing 的值是:", nothing, "→ 类型是:", type(nothing))

# 类型转换：你可以在不同类型之间转换
num_str = "123"         # 这是一个字符串 "123"，不是数字 123
num_int = int(num_str)  # 用 int() 把字符串转成整数
print('"123" 转成整数:', num_int, "→ 类型:", type(num_int))

price_float = float("19.99")   # 用 float() 把字符串转成小数
print('"19.99" 转成小数:', price_float)

# 反过来，数字也能转成字符串
age = 30
age_str = str(age)      # 用 str() 把数字转成字符串
print("年龄字符串:", age_str, "→ 类型:", type(age_str))

