"""
条件判断让程序根据不同的情况执行不同的代码。
就像你在生活中做决定：
  "如果（if）下雨，就带伞；
   否则如果（elif）阴天，就带帽子；
   否则（else），什么都不带。"

语法结构：
  if 条件:
      条件成立时执行的代码（注意前面要缩进！）
  elif 其他条件:
      第二个条件成立时执行的代码
  else:
      以上都不成立时执行的代码
"""

# 例子1：判断年龄
age = 17
print("年龄:", age)

if age >= 18:
    print("你是成年人，可以看这部电影 🎬")
elif age >= 13:
    print("你是青少年，需要家长陪同")
else:
    print("你还太小，不适合看这部电影")

# 例子2：判断成绩等级
score = 85
print("\n分数:", score)

if score >= 90:
    grade = "A (优秀)"
elif score >= 80:
    grade = "B (良好)"
elif score >= 70:
    grade = "C (中等)"
elif score >= 60:
    grade = "D (及格)"
else:
    grade = "F (不及格)"

print("等级:", grade)

# 例子3：嵌套 if（if 里面套 if）
has_money = True
price = 50
wallet = 100

print("\n商品价格:", price, "元，钱包里有:", wallet, "元")

if has_money:
    if wallet >= price:
        print("✅ 购买成功！剩余:", wallet - price, "元")
    else:
        print("😢 钱不够，还差", price - wallet, "元")
else:
    print("忘带钱包了！")

