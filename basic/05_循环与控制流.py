# 循环 —— 让程序"重复做一件事"
# ============================================================
print("\n" + "=" * 60)
print("循环")
print("=" * 60)

"""
什么是"循环"？
  循环就是让一段代码被重复执行多次，而不是复制粘贴很多遍。

  举个例子：如果让你打印 1 到 100，你会不会写 100 行 print()？
  用循环的话，只需要 2 行代码就能搞定！

Python 有两种主要的循环：
  1. for 循环 —— 用来"遍历"一个序列（一个接一个地处理里面的元素）
     比如：遍历一个字符串里的每个字符、遍历一个列表里的每个数字。
     你可以把它理解成："对于……里面的每一个，都做一遍"。

  2. while 循环 —— 只要某个条件成立，就一直循环下去
     比如："只要我还饿，就继续吃"、"只要倒计时还没到 0，就继续数"。
     你可以把它理解成："当……的时候，就做"。
"""

# ============================================================
# 一、for 循环 —— "对于……里面的每一个，都做一遍"
# ============================================================
print("\n--- for 循环 ---")

"""
for 循环的基本语法：
    for 变量名 in 序列:
        要重复执行的代码

  逐词解释：
    • for   —— Python 关键字，表示"对于"
    • 变量名 —— 你自己起的名字，每次循环它代表序列中的"当前这一个"元素
    • in    —— Python 关键字，表示"在……里面"
    • 序列  —— 可以是一个字符串、列表、range() 等（一系列东西排在一起）
    • :     —— 冒号，表示"下面是我要重复做的事情"
    • 缩进  —— 冒号下面的代码必须缩进（按 Tab 或 4 个空格），
              Python 靠缩进来判断哪些代码属于这个 for 循环的"身体"。

  用人话来说：对于 序列 里的 每一个元素，依次赋值给 变量名，
  然后执行缩进里的代码。
"""

# ----- 示例1：遍历字符串 -----
print("逐个打印 'Python' 的每个字符：")

"""
下面这行代码的意思是：
  对于字符串 "Python" 里的每一个字符，依次把它放到变量 letter 里，
  然后执行 print("  ", letter)

  执行过程：
    第 1 轮：letter = 'P'  →  print("  ", 'P')
    第 2 轮：letter = 'y'  →  print("  ", 'y')
    第 3 轮：letter = 't'  →  print("  ", 't')
    第 4 轮：letter = 'h'  →  print("  ", 'h')
    第 5 轮：letter = 'o'  →  print("  ", 'o')
    第 6 轮：letter = 'n'  →  print("  ", 'n')
  字符串里没东西了 → 循环结束
"""
for letter in "Python":
    print("  ", letter)

# ----- range() 详解 -----
"""
range() 是什么？

  range() 是 Python 内置的一个函数，用来生成一串数字。
  它不会直接生成所有数字（省内存），而是在循环里"用到的时候才给出来"。

  range() 有三种用法（参数个数不同，效果也不同）：

  ① range(终点)             → 从 0 开始，到 终点-1 结束，每次 +1
  ② range(起点, 终点)        → 从 起点 开始，到 终点-1 结束，每次 +1
  ③ range(起点, 终点, 步长)  → 从 起点 开始，到 终点-1 结束，每次 +步长

  ⚠️ 重要：range() 生成的数字 包含起点，不包含终点！
    这和数学里的"左闭右开区间 [起点, 终点)" 是一样的。
    为什么这样设计？因为计算机里大多数时候从 0 开始数，
    这样 range(5) 正好生成 5 个数字（0,1,2,3,4），很自然。
"""

# ① range(终点) —— 只有一个参数
print("\n打印 0 到 4：")

"""
range(5) 生成：0, 1, 2, 3, 4
  从 0 开始（默认），到 5 之前停止，所以是 5 个数字：0,1,2,3,4

  i 是变量名（习惯叫 i，是 index 的缩写），你也可以叫 num、n 等任何名字。
  每次循环，i 的值自动变成下一个数字。
"""
for i in range(5):
    print("  第", i, "次")

# ② range(起点, 终点) —— 两个参数
print("\n打印 2 到 6：")

"""
range(2, 7) 生成：2, 3, 4, 5, 6
  从 2 开始，到 7 之前停止
  注意：不包含 7！
"""
for i in range(2, 7):
    print("  数字:", i)

# ③ range(起点, 终点, 步长) —— 三个参数
print("\n打印 1 到 9 的奇数：")

"""
range(1, 10, 2) 生成：1, 3, 5, 7, 9
  从 1 开始，到 10 之前停止，每次 +2（步长为2）
  所以得到：1 → 3 → 5 → 7 → 9，全是奇数！

  步长也可以是负数，比如 range(10, 0, -1) 会生成 10, 9, 8, ..., 1
  （倒着数）
"""
for i in range(1, 10, 2):
    print("  奇数:", i)

# ----- 实战例子：乘法口诀表 -----
print("\n5 的乘法口诀：")

"""
这个例子展示了循环的实际用途。
对于 i = 1, 2, 3, ..., 9，每次都计算并打印 5 × i = 结果。
"""
for i in range(1, 10):
    print("  5 ×", i, "=", 5 * i)

# ============================================================
# 二、while 循环 —— "当……的时候，就一直做"
# ============================================================
print("\n--- while 循环 ---")

"""
while 循环的基本语法：
    while 条件:
        要重复执行的代码

  逐词解释：
    • while  —— Python 关键字，表示"当……的时候"
    • 条件   —— 一个布尔表达式（结果必须是 True 或 False）
    • :      —— 冒号，表示"条件成立时要做的事情在下面"
    • 缩进   —— 和 for 一样，冒号下面的代码必须缩进

  执行过程：
    ① 检查"条件"是否为 True
    ② 如果为 True  → 执行缩进里的代码，然后回到步骤①再次检查
    ③ 如果为 False → 跳过整个 while 块，继续执行后面的代码

  ⚠️ 关键：while 循环的缩进代码里，必须要有能"改变条件"的操作，
    让条件最终变成 False，否则就会变成"死循环"（永远停不下来）。
"""

# ----- 示例：倒数计时 -----
print("倒数计时：")

"""
下面这段代码的执行过程：
  初始：countdown = 5

  第 1 轮：countdown > 0 ？→ 5 > 0 是 True → 打印 "5 ..." → countdown 变成 4
  第 2 轮：countdown > 0 ？→ 4 > 0 是 True → 打印 "4 ..." → countdown 变成 3
  第 3 轮：countdown > 0 ？→ 3 > 0 是 True → 打印 "3 ..." → countdown 变成 2
  第 4 轮：countdown > 0 ？→ 2 > 0 是 True → 打印 "2 ..." → countdown 变成 1
  第 5 轮：countdown > 0 ？→ 1 > 0 是 True → 打印 "1 ..." → countdown 变成 0
  第 6 轮：countdown > 0 ？→ 0 > 0 是 False → 退出循环！
  然后执行 print("  🚀 发射！")
"""
countdown = 5                     # 初始值：倒计时从 5 开始
while countdown > 0:              # 条件：倒计时大于 0 就继续
    print("  ", countdown, "...") # 打印当前数字
    countdown = countdown - 1     # 把 countdown 减 1（每次循环都变小，最终会 ≤0）
    #                 ↑ 也可以简写成 countdown -= 1，意思完全一样
print("  🚀 发射！")

# ----- 死循环警告 -----
"""
⚠️ 什么是"死循环"？

  如果 while 的条件永远为 True，循环就永远不会停止。
  比如：

      while True:           # True 永远为 True，条件永远成立！
          print("我不会停！")

  这个程序会永远打印下去，直到你手动按 Ctrl+C 强制终止。

  所以写 while 循环时一定要注意：循环体里必须有让条件最终变成 False 的代码！

  下面这行被注释掉了（前面有 #），所以不会执行，只是给你看的反面教材：
"""
# while True:
#     print("我不会停！按 Ctrl+C 强制结束！")

# ============================================================
# 三、break 和 continue —— 循环的"遥控器"
# ============================================================
print("\n--- break 和 continue ---")

"""
break 和 continue 是循环里面两个特殊的"控制"语句。
它们让你能在循环中途改变循环的行为。

  一句话区分：
    • break    → 结束【整个循环】，后面的所有轮次都不执行了
    • continue → 跳过【当前这一轮】，循环本身继续，下一轮照常进行

  用人话打个比方（你在吃一包有 10 颗的糖）：
    • 吃到第 6 颗发现是坏的 → break    ：把整包糖扔掉，后面 7~10 号都不吃了
    • 吃到第 6 颗发现是坏的 → continue ：吐掉这颗，继续吃第 7、8、9、10 号
"""

# ============================================================
# 先看"正常情况"：不加 break 也不加 continue，打印 1~5
# ============================================================
print("① 正常循环 —— 打印 1 到 5：")
for i in range(1, 6):
    print("  ", i)
print("  ✅ 循环结束（打印了 5 个数）")

"""
上面就是最普通的循环：从 1 数到 5，一共跑了 5 轮，每轮都打印。
下面我们来对比 break 和 continue 的效果。
"""

# ============================================================
# break 演示：遇到 3 就停
# ============================================================
print("\n② 加 break —— 遇到 3 就「不玩了，走人」：")
for i in range(1, 6):
    if i == 3:
        print("  到 3 了，break！")
        break          # ← 整个循环到此为止！4 和 5 永远不会被执行
    print("  ", i)
print("  ⛔ 循环结束（只打印了 2 个数，后面 3、4、5 全跳过了）")

"""
上面这个例子里，break 之后发生了什么？
  i=1 → 打印 1
  i=2 → 打印 2
  i=3 → 条件成立 → 打印"到 3 了" → 执行 break → 循环直接结束！
  i=4 → ❌ 永远到不了
  i=5 → ❌ 永远到不了

  关键：break 之后，循环就"死"了，后面的 i=4、i=5 连看一眼都不会。
"""

# ============================================================
# continue 演示：遇到 3 就跳过（但 4、5 照常执行！）
# ============================================================
print("\n③ 加 continue —— 遇到 3 就「跳过，继续下一个」：")
for i in range(1, 6):
    if i == 3:
        print("  到 3 了，continue！跳过这一轮")
        continue       # ← 只跳过本轮剩余的代码，循环继续！
    print("  ", i)
print("  ✅ 循环结束（跳过了 3，但 4 和 5 都正常打印了）")

"""
上面这个例子里，continue 之后发生了什么？
  i=1 → 打印 1
  i=2 → 打印 2
  i=3 → 条件成立 → 打印"到 3 了" → 执行 continue
         → 跳过本轮的 print("  ", i)，直接进入下一轮
  i=4 → 打印 4          ← 循环还在继续！
  i=5 → 打印 5          ← 循环还在继续！

  关键：continue 只影响 i=3 这一轮，循环本身完好无损，i=4 和 i=5 照常执行。
"""

# ============================================================
# 🔥 核心对比：同样的条件 (i==3)，break vs continue 结果完全不同
# ============================================================
print("\n" + "─" * 50)
print("🔥 对比总结（都是 1~5，都是碰到 3）：")
print("─" * 50)
print("  正常循环：  1  2  3  4  5   ← 全部打印")
print("  加了 break：   1  2           ← 到 3 就彻底停了，4、5 没了")
print("  加了 continue：1  2    4  5   ← 只是 3 没了，4、5 照常打印")
print("─" * 50)
print("口诀：break 是「不干了走人」，continue 是「这个人跳过，下一个人进来」")
print("─" * 50)

# ============================================================
# 补充知识点 —— % 运算符（取余 / 取模）
# ============================================================
print("\n--- 补充：% 取余运算符 ---")

"""
上面演示 break/continue 用的是 == 来判断"等于 3"。
实际编程中，continue 经常和 %（取余）搭配，用来跳"特定规律的数"。

  % 的意思是"取余数"（也叫取模）：
    • 7 % 3 → 7 除以 3 商 2 余 1，所以结果是 1
    • 6 % 2 → 6 除以 2 商 3 余 0，所以结果是 0（偶数除以 2 余数一定是 0）
    • 5 % 2 → 5 除以 2 商 2 余 1，所以结果是 1（奇数除以 2 余数一定是 1）

  所以：i % 2 == 0  就是在问"i 是偶数吗？"
       i % 2 != 0  就是在问"i 是奇数吗？"
"""

# 用 continue + % 来跳过偶数
print("用 continue 和 % 跳过所有偶数：")
for i in range(1, 11):
    if i % 2 == 0:            # 如果 i 是偶数
        continue              # 跳过，不打印
    print("  ", i, "(奇数)")
print("✅ 你看，循环走完了 10 轮，只是偶数轮没打印而已，循环本身没有停！")

