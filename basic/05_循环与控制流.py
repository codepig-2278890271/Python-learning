# 四、嵌套循环 —— 循环里面套循环
# ============================================================
print("\n--- 嵌套循环 ---")

"""
什么是嵌套循环？

  嵌套循环就是"循环里面再写一个循环"。
  就像俄罗斯套娃一样，外层循环每跑一轮，内层循环就要跑完一整圈。

  语法上就是两个 for 缩进在一起：
      for 外层变量 in 外层序列:          ← 外层循环
          for 内层变量 in 内层序列:      ← 内层循环（多一层缩进）
              要重复执行的代码            ← 两层缩进

  执行顺序（这是理解嵌套循环最关键的地方！）：
    外层第 1 轮 → 内层跑完所有轮（1, 2, 3, ..., 最后一轮）
    外层第 2 轮 → 内层又跑完所有轮（1, 2, 3, ..., 最后一轮）
    外层第 3 轮 → 内层再跑完所有轮……
    ...直到外层也跑完

  用时钟来类比：
    • 外层是"小时"（1 点到 12 点）
    • 内层是"分钟"（0 分到 59 分）
    • 时钟每走 1 小时，分针要转完一整圈 60 分钟
    • 这就是嵌套循环！
"""

# ----- 示例1：最简单的嵌套循环（看执行顺序） -----
print("外层 3 轮 × 内层 2 轮，看看执行顺序：")
for outer in range(1, 4):          # 外层：1, 2, 3（共 3 轮）
    for inner in range(1, 3):      # 内层：1, 2（共 2 轮）
        print("  外层第", outer, "轮 → 内层第", inner, "轮")
print("✅ 总共跑了", 3 * 2, "次（外层 3 轮 × 内层 2 轮）")

"""
看上面的输出：外层每固定一个数字，内层就从头到尾跑一遍（1→2）。
这就是嵌套循环的核心规律：外层走一步，内层走全程。

总执行次数 = 外层循环次数 × 内层循环次数
"""

# ----- 示例2：打印完整的 9×9 乘法口诀表 -----
print("\n完整的 9×9 乘法口诀表：")

"""
这就是嵌套循环最经典的例子！
外层循环控制"行"（被乘数 1~9），内层循环控制"列"（乘数 1~行号）。

执行过程：
  外层 i=1：内层 j 从 1 到 1 → 打印 "1×1=1"
  外层 i=2：内层 j 从 1 到 2 → 打印 "1×2=2  2×2=4"
  外层 i=3：内层 j 从 1 到 3 → 打印 "1×3=3  2×3=6  3×3=9"
  ...
  外层 i=9：内层 j 从 1 到 9 → 打印 "1×9=9  2×9=18  ...  9×9=81"

注意：range(1, i+1) 的意思是内层 j 只从 1 到 i。
     因为乘法表是三角形的（第 i 行只要 i 列），
     这样打出来才是标准的"下三角"格式。

print() 里的 end="" 意思是"打印完不要换行"，
这样同一行的乘法算式就能排在一起了。
内层循环结束后，单独一个 print() 才是换行。
"""
for i in range(1, 10):                # i：行号，也是被乘数（1 到 9）
    for j in range(1, i + 1):         # j：列号，也是乘数（1 到 i）
        result = i * j
        print(j, "×", i, "=", result, end="  ")  # end="  " 表示不换行，用空格隔开
    print()                           # 内层结束 → 换行，开始下一行
    # ↑ 这个 print() 什么都没有，就是单纯换一行

"""
输出结果就是标准的乘法口诀表（三角形）：
  1 × 1 = 1
  1 × 2 = 2  2 × 2 = 4
  1 × 3 = 3  2 × 3 = 6  3 × 3 = 9
  ...（以此类推）

对比之前学的"单层循环打印 5 的乘法口诀"：
  单层循环：只能打印"一行"（5×1, 5×2, ..., 5×9）
  嵌套循环：能打印"一整张表"（1×1 到 9×9，共 9 行）
"""

# ----- 示例3：用嵌套循环打印图形 -----
print("\n用嵌套循环打印一个直角三角形：")

"""
外层循环控制行数（共 5 行），内层循环控制每行打印几个星星。
第 1 行 → 1 颗星，第 2 行 → 2 颗星，...，第 5 行 → 5 颗星。
"""
for row in range(2,6):               # row：第几行（1 到 5）
    for col in range(row):            # col：每行打印 row 颗星
        print("*", end="")            # 打印一颗星，不换行
    print()                           # 一行结束，换行

"""
输出结果：
  *
  **
  ***
  ****
  *****
"""

# ----- 示例4：用嵌套循环画一棵圣诞树 -----
print("\n用嵌套循环画一棵圣诞树：")

"""
圣诞树由两部分组成：
  ① 枝叶部分 —— 3 层三角形，每层由多行组成
  ② 树干部分 —— 树下面的矩形树干，起支撑作用

外层循环控制"层"（tier），内层循环控制每层的"行"（row）。

设计思路：
  • 第 1 层（最上面）每一行从 1 颗星开始逐渐变宽
  • 第 2 层继承第 1 层的宽度，继续变宽
  • 第 3 层再继承第 2 层的宽度，变得最宽
  • 这样整体看起来就是一棵上窄下宽的圣诞树！

关键公式：
  stars = 2 * (row + tier) + 1
  （每一行的星星数必须是奇数，这样树才对称好看）
  spaces = (max_stars - stars) // 2
  （用空格把每一行推到中间，树就"居中"了）
"""

# --- ① 画枝叶 ---
tiers = 3                # 3 层枝叶
height = 4               # 每层 4 行
max_stars = 2 * (height - 1 + tiers - 1) + 1   # 最宽处（最底层最后一行）的星星数

print("  🎄 圣诞树来啦：")
for tier in range(tiers):               # 外层：第几层（0, 1, 2）
    for row in range(height):           # 内层：这一层的第几行（0, 1, 2, 3）
        stars = 2 * (row + tier) + 1    # 这一行画几颗星星
        spaces = (max_stars - stars) // 2  # 左边要补多少空格（让树居中）
        print(" " * spaces + "*" * stars)

# --- ② 画树干 ---
trunk_height = 3         # 树干高 3 行
trunk_width = 3          # 树干宽 3 个字符
trunk_spaces = (max_stars - trunk_width) // 2   # 树干也要居中

for _ in range(trunk_height):           # 树干：重复 3 行
    print(" " * trunk_spaces + "|" * trunk_width)

