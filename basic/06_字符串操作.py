# ============================================================
# 字符串操作 —— 玩转文字
# ============================================================
print("\n" + "=" * 60)
print("字符串常用操作")
print("=" * 60)

# 字符串是 Python 中最常用的类型之一
s = "  Hello, Python World!  "
print("原始字符串:", repr(s))  # repr() 可以看到空格和真实内容

# 常用方法（方法就是"属于字符串的函数"，用 . 来调用）
print("\n--- 查找和判断 ---")
print("长度 len(s):         ", len(s))                    # 字符串长度
print("'Python' 在里面吗？   ", "Python" in s)             # 是否包含
print("以 'Hel' 开头？       ", s.strip().startswith("Hel"))  # 是否以xxx开头
print("以 'ld!' 结尾？       ", s.strip().endswith("ld!"))    # 是否以xxx结尾

print("\n--- 大小写转换 ---")
print("全部大写 s.upper():   ", s.upper())                 # 全大写
print("全部小写 s.lower():   ", s.lower())                 # 全小写
print("首字母大写 s.title():  ", s.title())                # 每个单词首字母大写

print("\n--- 清理和替换 ---")
print("去首尾空格 s.strip(): ", s.strip())                  # 去掉首尾空白
print("替换 s.replace():     ", s.replace("World", "中国")) # 替换文字

print("\n--- 分割和拼接 ---")
fruits = "苹果,香蕉,橘子,葡萄"
print("原文本:", fruits)
fruit_list = fruits.split(",")           # 用逗号分割成列表
print("分割后:", fruit_list)
print("拼接回去:", " + ".join(fruit_list))    # 用 " + " 把列表拼成字符串

# ----- f-string：格式化字符串（非常好用！推荐！）-----
print("\n--- f-string 格式化 ---")
name = "小明"
age = 25
height = 175.5

# 在字符串前面加 f，然后在 {} 里直接写变量名
print(f"我叫{name}，今年{age}岁，身高{height}厘米。")

# {} 里面还能做运算
price = 9.9
quantity = 3
print(f"{quantity} 个苹果，每个 {price} 元，一共 {price * quantity} 元")

# 控制小数位数
pi = 3.141592653589793
print(f"π 保留两位小数：{pi:.2f}")
print(f"π 保留四位小数：{pi:.4f}")

print("\n" + "=" * 60)
