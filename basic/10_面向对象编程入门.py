print("\n" + "=" * 60)
print("OOP 核心概念速查")
print("=" * 60)

# ------------------------------------------------------------
# 【核心概念一览】
#
#   类 (class)  →  蓝图/模板        class Dog:
#   对象 (object) →  蓝图造出的实物   dog1 = Dog("旺财", "金毛", 3)
#   属性          →  对象拥有的数据    self.name = name
#   方法          →  对象能做的事      def bark(self): ...
#
# ┌──────────────────┬──────────────────────────────────────────┐
# │      概念        │               一句话解释                   │
# ├──────────────────┼──────────────────────────────────────────┤
# │ class            │ 定义一类事物的属性和行为                    │
# │ __init__         │ 创建对象时自动调用，用来设置初始属性         │
# │ self             │ 代表"这个对象自己"，必须是方法的第一个参数   │
# │ 对象 = 类()      │ 用类创建实例，__init__ 的参数传给类()       │
# │ 继承 class B(A)  │ B 自动拥有 A 的所有属性和方法               │
# │ super()          │ 调用父类的方法，避免重复写代码              │
# │ 重写 (override)  │ 子类重新定义父类已有的方法，实现不同行为     │
# └──────────────────┴──────────────────────────────────────────┘
#
# 【self 到底是个啥？图解】
#
#   dog1.bark()
#   └─┬─┘ └─┬──┘
#     │     └── 方法，需要 self 参数
#     │
#     └── Python 自动把 dog1 传给 self
#         等价于 Dog.bark(dog1)
#
#   所以 def bark(self) 里的 self 就是 dog1 自己！
#   self.name 就是 dog1 的 name 属性。
#
# 【继承的执行流程】
#
#   guide_dog.bark()   →  先去 GuideDog 里找 bark()
#                         →  没找到？
#                         →  去父类 Dog 里找  →  找到了！执行 ✅
#
#   guide_dog.get_info() →  先去 GuideDog 里找  →  找到了！执行 ✅
#                           （不会再去父类了，因为子类已经重写了）
#
# 【C 语言对比】C 没有 OOP 概念，但可以类比：
#   class  ≈ struct（但 struct 只有数据，没有方法）
#   方法   ≈ 第一个参数是 struct 指针的函数
#   继承   ≈ C 做不到，只能手动组合（把父结构体放第一个字段）
# ------------------------------------------------------------

print("类 = 蓝图    →  class Dog:                     ← 定义狗长什么样")
print("对象 = 实物  →  dog1 = Dog('旺财', '金毛', 3)   ← 造一只真狗")
print("self = 自己  →  self.name 就是这只狗自己的名字")
print("继承 = 子承父业 → class GuideDog(Dog):         ← 导盲犬也是狗，多了新技能")

